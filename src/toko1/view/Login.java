    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Database.init(toko1.common.Const.getUrlDB(),toko1.common.Const.DB_USER,toko1.common.Const.DB_PASS);
        ResultSet result=Database.executeQuery("SELECT * FROM `user` WHERE id=\'"+id.getText()+"\' AND password=md5(concat(sha1(md5(\'"+new String(pas.getPassword())+"\')),sha1(sha1(\'toko1\'))))");
        try {
            if(result.first()){
                l_d.control.Login.init(result.getString("id"), result.getString("nama"));
                if(result.getBoolean("admin"))
                    new AdminView().setVisible(true);
                else
                    new StaffView().setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Username/Password salah");
            }
        } catch (SQLException ex) {
            new ErrorLog(this,ex,true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

